package com.pearl.fcw.lobby.pojo.json;

/**
 * 地图内推车粗略信息
 */
public class JsonLevelVehicle {
	private int addBlood;//加血？
	private float x;
	private float y;
	private float z;

	public int getAddBlood() {
		return addBlood;
	}

	public void setAddBlood(int addBlood) {
		this.addBlood = addBlood;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public float getZ() {
		return z;
	}

	public void setZ(float z) {
		this.z = z;
	}
}
