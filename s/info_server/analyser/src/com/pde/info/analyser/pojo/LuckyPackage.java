package com.pde.info.analyser.pojo;

import java.util.Date;

public class LuckyPackage {

	int id;
	int playerId;
	int packageLevel;
	int payType;
	int systemId;
	int systemNumber;
	int useType;
	int chipPrice;
	Date createdate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPlayerId() {
		return playerId;
	}
	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}
	public int getPackageLevel() {
		return packageLevel;
	}
	public void setPackageLevel(int packageLevel) {
		this.packageLevel = packageLevel;
	}
	public int getPayType() {
		return payType;
	}
	public void setPayType(int payType) {
		this.payType = payType;
	}
	public int getSystemId() {
		return systemId;
	}
	public void setSystemId(int systemId) {
		this.systemId = systemId;
	}
	public int getSystemNumber() {
		return systemNumber;
	}
	public void setSystemNumber(int systemNumber) {
		this.systemNumber = systemNumber;
	}
	public int getUseType() {
		return useType;
	}
	public void setUseType(int useType) {
		this.useType = useType;
	}
	public Date getCreatedate() {
		return createdate;
	}
	public void setCreatedate(Date createdate) {
		this.createdate = createdate;
	}
	public int getChipPrice() {
		return chipPrice;
	}
	public void setChipPrice(int chipPrice) {
		this.chipPrice = chipPrice;
	}

}
